int	get_size(int nb)
{
	int				i;
	unsigned int	abs;

	if (nb < 0)
		abs = nb * -1;
	else
		abs = nb;
	i = 1;
	while (abs > 9)
	{
		abs = abs / 10;
		i++;
	}
	return (i);
}
